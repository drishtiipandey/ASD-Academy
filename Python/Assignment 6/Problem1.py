# Step 1: Create a class called Animal
class Animal:
    def __init__(self, name, species):
        self.name = name       # Attribute: name
        self.species = species # Attribute: species
    # Step 3: Method to make a sound
    def make_sound(self):
        print("Some generic sound")
# Step 4: Create an object of Animal and call the method
animal1 = Animal("Generic Animal", "Unknown")
animal1.make_sound()  # Output: Some generic sound
# Step 5: Create child classes Dog and Cat
class Dog(Animal):
    # Step 6: Override the make_sound() method
    def make_sound(self):
        print("Woof!")
class Cat(Animal):
    # Step 6: Override the make_sound() method
    def make_sound(self):
        print("Meow!")
# Step 7: Create one object of each subclass and call make_sound()
dog1 = Dog("Buddy", "Dog")
cat1 = Cat("Whiskers", "Cat")
dog1.make_sound()  # Output: Woof!
cat1.make_sound()  # Output: Meow!
